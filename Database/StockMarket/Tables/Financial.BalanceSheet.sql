    OtherCurrentLiabilities DECIMAL(15,2) NULL,
    OtherNonCurrentLiabilities DECIMAL(15,2) NULL,
    TotalShareholderEquity DECIMAL(15,2) NULL,
    TreasuryStock DECIMAL(15,2) NULL,
    RetainedEarnings DECIMAL(15,2) NULL,
    CommonStock DECIMAL(15,2) NULL,
    CommonStockSharesOutstanding DECIMAL(15,2) NULL,

    Constraint FK_BalanceSheet_Stock FOREIGN KEY (StockId) 
