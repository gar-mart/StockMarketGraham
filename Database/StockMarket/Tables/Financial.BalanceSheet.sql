    CurrentNetReceivables DECIMAL(15,2) NULL,
    TotalNonCurrentAssets DECIMAL(15,2) NULL,
    PropertyPlantEquipment DECIMAL(15,2) NULL,
    AccumulatedDepreciationAmortizationPPE DECIMAL(15,2) NULL,
    IntangibleAssets DECIMAL(15,2) NULL,
    IntangibleAssetsExcludingGoodwill DECIMAL(15,2) NULL,
    Goodwill DECIMAL(15,2) NULL,
    Investments DECIMAL(15,2) NULL,
    LongTermInvestments DECIMAL(15,2) NULL,
    ShortTermInvestments DECIMAL(15,2) NULL,
    OtherCurrentAssets DECIMAL(15,2) NULL,
